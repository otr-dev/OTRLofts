function createGallery(gallery) {
  return `<div class="_gallary">
              <div class="owl-carousel-${gallery.name} owl-carousel owl-theme"> 
                ${createArticles(gallery)}
              </div>
              <button id="owl-carousel-prev-${
                gallery.name
              }" class="btn btn--previus">
                <ion-icon size="large" name="chevron-back-outline"></ion-icon>
              </button>
              <button id="owl-carousel-next-${
                gallery.name
              }" class="btn btn--next">
                <ion-icon
                  size="large"
                  name="chevron-forward-outline"
                ></ion-icon>
              </button>
            </div>
              `;
}

function createArticles(gallery) {
  let articles = "";

  for (let i = 0; i < gallery.images.length; i++) {
    const image = gallery.images[i];
    articles += `
    <article id="carousel-${gallery.name}-item-${i}" class="_card">
        <div class="_card__image">
            <div class="_image">
                <div class="img">
                    <picture>
                      <source
                        class="owl-lazy"
                        data-srcset="assets/images/${image.path}_512.webp"
                        type="image/webp"
                      />
                      <img
                        class="owl-lazy"
                        data-srcset="assets/images/${image.path}_512.jpg"
                        alt="${image.alt}"
                      />
                    </picture>
                </div>
            </div>
        </div>
    `;

    if (gallery.titleVisible) {
      articles += `
        <h5 class="_card__title _t _t--semi-bold _t--20">${image.title}</h5>
        `;
    }

    if (gallery.subtitleVisible) {
      articles += `
        <span class="_card__subtitle _t _t--18">${image.subtitle}</span>
        `;
    }

    articles += `</article>`;
  }

  return articles;
}

function openGalleryModal(gallery, index) {
  const modal = document.getElementById("modal");
  modal.innerHTML = createModal(gallery);
  owlModal = $(`.owl-carousel-modal-${gallery.name}`);
  owlModal.owlCarousel({
    items: 1,
    margin: 30,
    loop: false,
    nav: false,
    autoHeight: true,
  });
}

function createModal(gallery) {
  return `
   <section class="_modal-gallery">
        <div class="content">
        <div class="_gallary">
          <div class="owl-carousel-modal-${
            gallery.name
          } owl-carousel owl-theme">
           ${createArticles(gallery)}
          </div>
          <button id="owl-carousel-modal-prev-${
            gallery.name
          }" class="btn btn--previus">
                <ion-icon size="large" name="chevron-back-outline"></ion-icon>
              </button>
              <button id="owl-carousel-next-modal-${
                gallery.name
              }" class="btn btn--next">
                <ion-icon
                  size="large"
                  name="chevron-forward-outline"
                ></ion-icon>
              </button>
        </div>
      </div>
      <button id="btnCloseGalleryModal" class="close">
        <ion-icon name="close-outline"></ion-icon>
      </button>
    </section>
  `;
}

function closeGalleryModal() {
  const modal = document.getElementById("modal");
  owlModal = null;
  modal.innerHTML = "";
}
